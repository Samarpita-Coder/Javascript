//Primitive Datatypes

//string
let s = "Sam";
console.log(s);
console.log(typeof(s));

//number
let n = 5;
console.log(n);
console.log(typeof(n));

//boolean
let b = true;
console.log(b);
console.log(typeof(b));

//null
let nl = null;
console.log(nl);
console.log(typeof(nl));

//defined
let u = undefined;
console.log(u);
console.log(typeof(u));

